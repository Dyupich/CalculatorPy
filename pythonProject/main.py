print("Введите число а")
a = float(input())
print("Введите число b")
b = float(input())
print("Введите операцию")
c = input()

if c == '+':
    print("Результат: ", a + b)
if c == '-':
    print("Результат: ", a - b)
if c == '/':
    print("Результат: ", a / b)
if c == '*':
    print("Результат: ", a * b)
if c == '%':
    print("Результат: ", a % b)
